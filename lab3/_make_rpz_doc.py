def set_table(tbl, rows):
    while len(tbl.rows) < len(rows):
        tbl.add_row()
    while len(tbl.rows) > len(rows):
        tbl._tbl.remove(tbl.rows[-1]._tr)

    for r_i, row_vals in enumerate(rows):
        for c_i, val in enumerate(row_vals):
            tbl.rows[r_i].cells[c_i].text = val


